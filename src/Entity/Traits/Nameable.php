<?php

namespace Smart\ContentBundle\Entity\Traits;

use Doctrine\Orm\Mapping as ORM;

trait Nameable
{
    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    protected $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
